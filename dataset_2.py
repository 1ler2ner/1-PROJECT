import pandas as pd

# ============================================================
# 1) ЗАГРУЗКА ДАННЫХ ИЗ EXCEL
# ============================================================

# Читаем Excel-файл в DataFrame.
# - sheet_name="10.25"  : имя листа в Excel (например, дата/раздел)
# - skiprows=4          : пропускаем первые 4 строки (обычно это шапка/служебные строки)
# - decimal=","         : в файле дробная часть записана через запятую (например 330,5)
data = pd.read_excel(
    r"D:\ALEX\ICS\U_1min_2025.xlsx",
    sheet_name="10.25",
    skiprows=4,
    decimal=","
)

# ============================================================
# 2) ВЫБОР НУЖНЫХ СТОЛБЦОВ (ВРЕМЯ + НАПРЯЖЕНИЕ)
# ============================================================

# В исходном Excel некоторые столбцы могут не иметь заголовков,
# поэтому pandas назначает им имена вида "Unnamed: N".
#
# Здесь мы берём:
# - "Unnamed: 2" : время/метка времени (timestamp)
# - "Unnamed: 8" : измерение напряжения линии BALTI–STRASENI
balti_str = pd.DataFrame({
    "timestamp": data["Unnamed: 2"],
    "BALTI-STRASENI": data["Unnamed: 8"]
})

# Делаем timestamp индексом таблицы.
# Это удобно, потому что:
# - временная метка становится "подписью строки"
# - проще находить события по времени, делать срезы, искать максимум и т.д.
balti_str = balti_str.set_index("timestamp")

# (Опционально, но полезно) Сортируем по времени на случай, если в файле строки перемешаны.
# Для анализа инцидентов порядок важен.
balti_str = balti_str.sort_index()

# ============================================================
# 3) БЫСТРАЯ ПРОВЕРКА, ЧТО ДАННЫЕ ЗАГРУЗИЛИСЬ ПРАВИЛЬНО
# ============================================================

# Разрешаем выводить много строк (осторожно: большие таблицы могут "заспамить" консоль)
pd.set_option("display.max_rows", None)

# Выводим первые 10 строк — это "контроль качества" загрузки
print(balti_str.head(10))

# ============================================================
# 4) МАКСИМУМ ПО ВСЕМУ ДНЮ (ИЛИ ПО ВСЕМУ ЛИСТУ)
# ============================================================

# max()  -> максимальное значение напряжения
# idxmax() -> индекс (время), когда это максимальное значение произошло
max_value = balti_str["BALTI-STRASENI"].max()
max_time = balti_str["BALTI-STRASENI"].idxmax()

print("Max value:", max_value)
print("At time:", max_time)

# describe() даёт базовую статистику: count/mean/std/min/квартили/max
print(balti_str["BALTI-STRASENI"].describe())

# ============================================================
# 5) ПОРОГ ПРЕВЫШЕНИЯ (110% ОТ НОМИНАЛА)
# ============================================================

Unom = 330                 # номинальное напряжение (кВ)
threshold = 1.1 * Unom     # порог превышения (110% от номинала), т.е. 363 кВ

# ============================================================
# 6) ПОИСК ИНЦИДЕНТОВ (ЭПИЗОДОВ) ПРЕВЫШЕНИЯ
# ============================================================

# Берём Series (один столбец) напряжения:
s = balti_str["BALTI-STRASENI"]

# above — логическая маска True/False для каждой минуты:
# True  -> в эту минуту напряжение выше порога
# False -> в эту минуту напряжение не выше порога
above = s > threshold

# starts — точки начала инцидента.
# Логика:
# - сейчас выше порога (above=True)
# - а в строке выше (то есть минуту назад) было НЕ выше порога
#
# above.shift(1)  : "в каждой строке взять значение above из строки выше"
# fill_value=False: для самой первой строки считаем, что "минуту назад превышения не было"
starts = above & ~above.shift(1, fill_value=False)

# incident_id — номер инцидента для каждой строки времени.
# starts — это True только на стартах, поэтому:
# - starts.cumsum() увеличивается на 1 каждый раз, когда встречается старт
# Пример:
# starts:      F F T F F T F ...
# cumsum():    0 0 1 1 1 2 2 ...
incident_id = starts.cumsum()

# exceed — таблица только тех минут, где было превышение.
# (То есть берём из balti_str только строки, где above=True)
exceed = balti_str.loc[above].copy()

# Добавляем каждой "минуте превышения" номер инцидента.
# Важно: incident_id[above] берёт номера инцидентов только для строк превышения
exceed["incident_id"] = incident_id[above]

# ============================================================
# 7) СВОДНАЯ ТАБЛИЦА ПО ИНЦИДЕНТАМ
# ============================================================

# groupby("incident_id") разбивает exceed на группы:
# - группа 1: все минуты подряд, которые относятся к инциденту №1
# - группа 2: все минуты подряд, которые относятся к инциденту №2
# и т.д.
#
# Затем agg() считает нужные показатели для каждой группы.
incidents = (
    exceed.groupby("incident_id")
    .agg(
        # Время начала инцидента = самое раннее время (минимальный индекс) внутри группы
        start_time=("BALTI-STRASENI", lambda x: x.index.min()),

        # Время конца инцидента = самое позднее время (максимальный индекс) внутри группы
        # (это последняя минута, где ещё было выше порога)
        end_time=("BALTI-STRASENI", lambda x: x.index.max()),

        # Длительность инцидента в "количестве строк".
        # Если шаг данных 1 минута, то это = количество минут.
        minutes=("BALTI-STRASENI", "size"),

        # Максимальное напряжение внутри инцидента
        max_kv=("BALTI-STRASENI", "max"),

        # Время, когда достигнут максимум внутри инцидента
        max_time=("BALTI-STRASENI", lambda x: x.idxmax()),

        # Среднее напряжение внутри инцидента (добавлено по твоей просьбе)
        mean_kv=("BALTI-STRASENI", "mean"),
    )
    .reset_index()
)

print("\n=== INCIDENTS (episodes above threshold) ===")
print(incidents)

# ============================================================
# 8) ДОПОЛНИТЕЛЬНО (ОПЦИОНАЛЬНО): СКОЛЬКО ВСЕГО МИНУТ ПРЕВЫШЕНИЙ
# ============================================================

print("\nTotal minutes above threshold:", len(exceed))
print("Threshold (kV):", threshold)